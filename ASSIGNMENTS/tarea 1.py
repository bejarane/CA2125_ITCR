#matriz de 5x8
n=5
m=8
matriz = []
lista = ['a','b','c','d','e','f','g','h','i','j','k','l']

for i in range(0,m):
    fila = []
    for j in range(0,n):
        fila.append(lista[j]+str(i))
    matriz.append(fila)

for f in matriz:
    print(f)
